package instructor.lesson_2.demo_2;

public class Calculator {

    public static void main(String[] args) {

        int a = 10;
        int b = 15;
        int sum = 10 + 15;

        System.out.println("-= The Sum =-");
        System.out.print(a);
        System.out.print(" + ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.print(sum);

        System.out.println();

        System.out.println(a + " + " + b + " = " + sum);

        System.out.println(7 + "+" + 3 + "=" + (7 + 3));

    }
}
